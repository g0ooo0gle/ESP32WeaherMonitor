/**
 * ESP32 Weather Station - ニューステキストティッカー ヘッダ
 *
 * [このファイルの役割]
 * ticker.cpp で実装する「テキストスクロール表示（ティッカー）」機能の
 * 関数・定数・変数の宣言をまとめています。
 *
 * [ティッカー機能の概要]
 * NHKニュースのRSSフィード（XML）を取得し、見出しを抜き出して
 * 画面下部をスクロールするテロップとして表示します。
 *
 * [スクロールの仕組み]
 * - ニュース見出しを1本の長い文字列としてつなぎます（区切り文字: "  ★  "）
 * - その文字列を毎フレーム少しずつ左にずらして描画（差分描画でちらつき防止）
 * - 文字列の末尾まで来たら先頭に戻り、無限にループします
 *
 * [依存関係]
 * config.h / display.h（tft, u8g2）/ network.h（WiFi状態確認）
 */

#pragma once  // 二重インクルード防止（#ifndef〜#endif の代わり）

#include "config.h"

// ================================================================
// ティッカー設定定数
// ================================================================

// スクロール速度（ループ1回あたりのピクセル移動量）
// 大きいほど速く動く。2〜4 が読みやすい。
#define TICKER_SCROLL_SPEED   2

// スクロール更新間隔（ミリ秒）
// この間隔でループが発火するたびに TICKER_SCROLL_SPEED px ずつ動く。
// 40ms + 2px/回 = 50px/秒 のスクロール速度
#define TICKER_UPDATE_INTERVAL  40

// RSSニュース再取得間隔（ミリ秒）: 5分ごと
// 頻繁に取得するとAPIに負荷をかけるので、5分程度が適切です。
#define TICKER_FETCH_INTERVAL   (5UL * 60 * 1000)

// ティッカーエリアのY座標と高さ（画面下部に配置）
// config.h のレイアウト定数と合わせてください。
// ST7735S の画面サイズは 128×160px（縦向き）です。
#define TICKER_AREA_Y     140   // ティッカーエリアの上端Y座標（下から20px分）
#define TICKER_AREA_H      20   // ティッカーエリアの高さ（px）
#define TICKER_AREA_W     128   // ティッカーエリアの幅（画面全体）

// ティッカー背景色（黒）と文字色（黄色）
// RGB565 形式。黒背景に黄色テキストは視認性が高いです。
#define TICKER_BG_COLOR   ST77XX_BLACK
#define TICKER_TEXT_COLOR ST77XX_YELLOW

// ================================================================
// 関数宣言（ticker.cpp で実装）
// ================================================================

/**
 * ティッカーを初期化して最初のニュースを取得します。
 * setup() の最後に1回だけ呼んでください。
 */
void setupTicker();

/**
 * ティッカーのスクロール描画・定期ニュース更新を担当します。
 * loop() の中で毎回呼んでください（内部でタイマー管理しています）。
 */
void updateTicker();

/**
 * NHKニュースのRSSを取得して見出しを tickerText に格納します。
 * setupTicker() と updateTicker() から内部的に呼ばれます。
 * 直接呼ぶ必要は通常ありません。
 */
void fetchNews();

// ================================================================
// 外部から参照できる状態変数（ticker.cpp で実体定義）
// ================================================================

/**
 * 現在スクロール表示中のテキスト（見出しを「  ★  」でつなげた文字列）
 * 他のファイルから参照したい場合は extern 宣言してください。
 */
extern String tickerText;

/**
 * ティッカーのスクロール位置（ピクセル単位）
 * 0 = テキストの先頭が画面左端から表示される状態
 * 増加するほどテキストが左にずれます。
 */
extern int tickerScrollX;

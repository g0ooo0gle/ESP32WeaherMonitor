/**
 * ticker.h - ニュース画面ヘッダ
 *
 * NHK RSS を Core 0 バックグラウンドタスクで取得し、
 * 見出しを 1 ページずつ全画面表示します。
 *
 * [画面レイアウト]
 *   Y=  0〜19  : タイトルバー（"ニュース N/M"）
 *   Y= 22〜111 : 見出し（タイトル）を折り返し静止表示（最大 5 行）
 *   Y=112      : 仕切り線
 *   Y=114〜159 : 記事概要を電光掲示板スクロール
 *
 * [ページ遷移]
 *   記事概要のスクロールが 1 周したあと、自動的に次のページへ移行します。
 *   NEXT ボタンで手動スキップも可能です。
 */

#pragma once

#include "config.h"

// ----------------------------------------------------------------
// バッファサイズ定数
// NHK 主要ニュース cat0 は 7〜15 件程度。20 件あれば余裕。
// ----------------------------------------------------------------
#define NEWS_MAX_ITEMS   20
#define NEWS_TITLE_MAX  200   // 1 見出しの最大バイト数
#define NEWS_DESC_MAX   400   // 1 概要の最大バイト数

// ----------------------------------------------------------------
// 関数宣言
// ----------------------------------------------------------------

/** setup() で 1 回だけ呼ぶ。Core 0 取得タスクを起動する */
void setupNews();

/** ニュース画面を描画する（画面切替・ボタン操作時に呼ぶ） */
void drawNewsScreen();

/** loop() からニュース画面表示中だけ呼ぶ。スクロールとページ送りを処理する */
void updateNewsAutoPaging();

/** 次の見出しへ手動で進める（NEXT 短押し） */
void nextNewsPage();

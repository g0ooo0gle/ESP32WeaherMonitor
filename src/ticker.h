/**
 * ESP32 Weather Station - ニュース画面 ヘッダ
 *
 * [大きな変更点]
 *   旧: ティッカー (画面下部20pxで横スクロール)
 *   新: 全画面ニュース表示
 *       1見出しずつページ表示、5秒で次へ自動切替、ボタンで手動切替も可能
 *
 * [機能分離]
 *   - 天気画面では ニュース機能は走らない (描画コストゼロ)
 *   - ニュース画面では 天気更新が止まらないよう loop で並行動作
 *   - RSS 取得は引き続き Core 0 タスクで非同期実行
 *
 * [見出しの管理]
 *   配列で複数見出しを保持し、currentNewsIndex でどれを描くかを決定。
 *   各見出しは固定長バッファで、UTF-8 バリデーション済み。
 */

#pragma once

#include "config.h"

// ----------------------------------------------------------------
// バッファサイズと最大見出し数
//
// NHK 主要ニュース cat0 は 7-15件程度なので、20件あれば余裕です。
// 1見出しは長くても 200 バイト前後なので、200 確保すれば十分。
// ----------------------------------------------------------------
#define NEWS_MAX_ITEMS    20
#define NEWS_TITLE_MAX   200
#define NEWS_DESC_MAX    400

// ----------------------------------------------------------------
// 関数宣言
// ----------------------------------------------------------------

/** setup() の最後で1回だけ呼ぶ。Core 0 取得タスクを起動 */
void setupNews();

/**
 * ニュース画面を描画する (画面切替時、ページ切替時に呼ぶ)
 * 内部で現在の見出しインデックス newsIndex を見て描画。
 */
void drawNewsScreen();

/**
 * loop() からニュース画面表示中だけ呼ぶ。
 * 5秒経過していたら次の見出しへ自動切替して再描画。
 */
void updateNewsAutoPaging();

/** 次の見出しへ手動で進める (CITY短押し) */
void nextNewsPage();

/** 前の見出しへ戻す (CITY長押し) */
void prevNewsPage();

/**
 * ESP32 Weather Station - 描画機能
 *
 * ディスプレイへのすべての描画処理をこのファイルにまとめています。
 * 時計、都市名、気温、天気説明、アイコン、静的要素など、
 * 画面に表示するものはここから呼び出されます。
 *
 * ポイント: 差分描画でちらつきを防いでいる。
 * 毎回画面全体をクリアするのではなく、「変化した部分だけ」を描画しています。
 * この方式で、画面の残像やちらつきがなく、かつ処理速度も速くなります。
 */

#ifndef DISPLAY_H
#define DISPLAY_H

#include "config.h"
#include "weather.h"

// ------------------------------------------------------------------
// 状態管理変数（天気情報取得で更新される）
// 現在表示している都市と天気の状態を保持します。
// ------------------------------------------------------------------
extern int cityIndex;                     // 現在表示中の都市インデックス
extern float currentTemp;                 // 現在の気温
extern int currentWeatherCode;            // 現在のWMO天気コード

// ------------------------------------------------------------------
// 差分検出用キャッシュ
// 各エリアの「前回の描画値」を保持しています。
// 前回と同じ値の場合は描画をスキップすることで、ちらつき防止と
// 処理負荷の低減を実現しています。
// ------------------------------------------------------------------
extern char prevTimeStr[16];              // 前回描画した時刻文字列
extern int prevCityIndex;                 // 前回の都市インデックス（-1 = 未描画）
extern float prevTemp;                    // 前回の気温（-999 = ありえない値で初回強制描画）
extern int prevWeatherCode;               // 前回の天気コード（-1 = 未描画）

// ------------------------------------------------------------------
// タイマー管理変数（loop() で millis() 比較に使用）
// ------------------------------------------------------------------
extern unsigned long lastFetchAttempt;    // API通信の前回試行時刻
extern unsigned long lastCitySwitch;      // 都市切替の前回実行時刻
extern unsigned long lastClockUpdate;     // 時計描画の前回実行時刻

// ------------------------------------------------------------------
// 指定エリアを背景色（デフォルト黒）で塗りつぶすヘルパー
// 再描画前に古い内容を消すために使います。画面全体をクリアするより
 // 高速です。
// ------------------------------------------------------------------
inline void clearArea(int y, int h, uint16_t color = ST77XX_BLACK)
{
  tft.fillRect(0, y, 128, h, color);
}

// ------------------------------------------------------------------
// 時計エリアを描画
// NTPから取得した現在時計を更新します。
// 差分描画対応: 値が変化していない場合は何もしません。
// ------------------------------------------------------------------
void drawClock();

// ------------------------------------------------------------------
// 都市名エリアを描画
// 現在表示中の都市名を描画します。都市切替のたびに呼ばれます。
// ------------------------------------------------------------------
void drawCity();

// ------------------------------------------------------------------
// 気温エリアを描画
// 天気アイコンと気温を表示します。天気データ更新時に呼ばれます。
// ------------------------------------------------------------------
void drawTemp();

// ------------------------------------------------------------------
// 天気説明エリアを描画
// 天気の日本語テキストを描画します。天気データ更新時に呼ばれます。
// ------------------------------------------------------------------
void drawWeather();

// ------------------------------------------------------------------
// 気温・天気説明をまとめて更新
// 背景色、都市名、気温、天気説明を一括で描画・キャッシュ更新します。
// 都市切替時・API通信でデータ変更時に呼びます。
// ------------------------------------------------------------------
void drawWeatherInfo();

// ------------------------------------------------------------------
// 起動時に一度だけ呼ぶ静的要素の描画
// 仕切り線など、変化しない要素を描画します。初期表示時のみ呼びます。
// ------------------------------------------------------------------
void drawStaticElements();

#endif // DISPLAY_H

/**
 * ESP32 Weather Station - 天気関連機能
 *
 * このファイルでは「天気コードの日本語変換」「背景色の判定」「天気アイコン描画」を
 * 実行しています。Open-Meteo APIから取得するWMO天気コード（数値）を、
 * 人間が理解しやすい形に変換・表示する役割を担っています。
 */

#ifndef WEATHER_H
#define WEATHER_H

#include "config.h"

// ------------------------------------------------------------------
// WMO天気コードを日本語テキストに変換
// Open-Meteoが採用している国際基準のWMOコードをすべて日本語に訳しています。
// code: Open-Meteoから取得した数値の天気コード
// 戻り値: 日本語の天気説明文字列
// ------------------------------------------------------------------
String getWeatherJp(int code);

// ------------------------------------------------------------------
// 天気コードから背景色を判定
// 天気コードをグループ（快晴・雨・雪・雷・霧・曇り）に分け、
// それぞれに対応する背景色を返します。
// code: Open-Meteoから取得した数値の天気コード
// 戻り値: RGB565形式の背景色
// ------------------------------------------------------------------
uint16_t getBgColor(int code);

// ------------------------------------------------------------------
// 天気アイコンを描画（図形プリミティブによる描画）
// 36x36pxの範囲内で、その天気を表すアイコンを円や線を使って描きます。
// x, y: アイコンを描画する画面の左上座標
// code: Open-Meteoから取得した数値の天気コード
// ------------------------------------------------------------------
void drawWeatherIcon(int x, int y, int code);

#endif // WEATHER_H

/**
 * ESP32 Weather Station - 物理ボタン制御 ヘッダ
 *
 * [このファイルの役割]
 * button.cpp で実装する「物理ボタンによる操作」機能の
 * 関数・定数・変数の宣言をまとめています。
 *
 * [ボタン配線メモ]
 * ボタンの一端を GPIO ピンに、もう一端を GND に接続します。
 * INPUT_PULLUP モードを使うので外付け抵抗は不要です。
 * ボタンを押していない → HIGH（プルアップで 3.3V）
 * ボタンを押している  → LOW（GND に接続）
 *
 * [使用ピン（config.h で変更可能）]
 * BTN_MODE_PIN  : 表示モードを切り替えるボタン（GPIO 0 = ENボタン）
 * BTN_CITY_PIN  : 都市を手動で次に進めるボタン（GPIO 35 = 入力専用ピン）
 *
 * [表示モード一覧]
 * MODE_WEATHER : 通常の天気表示（デフォルト）
 * MODE_TICKER  : ニューステキストのみ拡大スクロール（準備中）
 *
 * [チャタリング対策]
 * 機械式ボタンは押した瞬間に HIGH/LOW が数ms間ぶれます（チャタリング）。
 * 50ms のデバウンス処理で誤認識を防ぎます。
 */

#pragma once

#include "config.h"

// ================================================================
// ボタンに割り当てる GPIO ピン番号
// 使用しているピンと競合しないように注意してください。
// GPIO 0  = ENボタン（内蔵）: フラッシュ書き込みモード切替と兼用ですが
//           通常動作中はボタンとして使えます。
// GPIO 35 = 入力専用ピン（OUTPUT不可）: 外付けボタン向けに最適です。
// ================================================================
#define BTN_MODE_PIN   0    // モード切替ボタン（GPIO 0）
#define BTN_CITY_PIN  35    // 都市切替ボタン  （GPIO 35）

// チャタリング除去の待機時間（ミリ秒）
// 50ms あれば機械式ボタンのほぼすべてのチャタリングを防げます。
#define BTN_DEBOUNCE_MS  50

// ================================================================
// 表示モード定義
// ================================================================

/**
 * 表示モードを表す列挙型（enum class）
 *
 * enum class を使うと他の enum と名前が衝突しません。
 * 例: DisplayMode::WEATHER と書くので、
 *     他の場所で WEATHER という変数名を使っても問題ありません。
 */
enum class DisplayMode {
  WEATHER,   // 通常の天気表示（都市名・気温・天気説明）
  TICKER     // ティッカー全画面表示（将来拡張用）
};

// ================================================================
// 関数宣言（button.cpp で実装）
// ================================================================

/**
 * ボタンのピンを入力モードで初期化します。
 * setup() の中で1回だけ呼んでください。
 */
void setupButtons();

/**
 * ボタンの状態を確認し、押されていれば対応するアクションを実行します。
 * loop() の中で毎回呼んでください（内部でデバウンス処理します）。
 */
void updateButtons();

// ================================================================
// 外部から参照できる状態変数（button.cpp で実体定義）
// ================================================================

/**
 * 現在の表示モード
 * 他のファイルから参照・変更可能です（例: main.cpp の loop() でモードに応じた処理）。
 */
extern DisplayMode currentMode;

/**
 * ESP32 Weather Station - 物理ボタン制御 ヘッダ
 *
 * [今回の変更点]
 *   - 画面分離に伴い Screen 列挙型を新設
 *       SCR_WEATHER: 天気画面
 *       SCR_NEWS   : ニュース画面
 *   - DisplayMode (全国/1都市) は WEATHER画面内のサブモードとして残す
 *   - ボタン割当を整理:
 *       MODE 短押し: 画面トグル (天気 ⇔ ニュース)
 *       MODE 長押し: 天気画面で「全国 ⇔ 1都市」をトグル
 *       CITY 短押し: 画面と状況に応じて (詳細を下記)
 *       CITY 長押し: 1都市モード時は都市送り
 *
 * [ボタン動作の早見表]
 *
 *   ◆ WEATHER画面
 *     - MODE 短押し → NEWS画面へ
 *     - MODE 長押し → 全国巡回 ⇄ 1都市詳細
 *     - CITY 短押し:
 *         全国モード   → 次の都市へ手動送り
 *         1都市モード → 週間 ⇄ 毎時 をトグル
 *     - CITY 長押し:
 *         1都市モード → 次の都市へ手動送り
 *
 *   ◆ NEWS画面
 *     - MODE 短押し → WEATHER画面へ
 *     - MODE 長押し → 無効
 *     - CITY 短押し → 次の見出しへ
 *     - CITY 長押し → 前の見出しへ
 */

#pragma once

#include "config.h"

// ================================================================
// ボタン GPIOピン
// ================================================================
#define BTN_MODE_PIN   0
#define BTN_CITY_PIN  35

// チャタリング除去の待機時間
#define BTN_DEBOUNCE_MS    50

// 長押し判定のしきい値（500ms）
#define BTN_LONGPRESS_MS   500

// ================================================================
// 画面の種類
// ================================================================
enum class Screen {
  WEATHER,   // 天気画面
  NEWS       // ニュース画面
};

// ================================================================
// WEATHER画面内のモード（全国 or 1都市）
// ================================================================
enum class DisplayMode {
  ALL_CITIES,   // 全国巡回モード
  SINGLE        // 1都市詳細モード
};

// ================================================================
// 1都市詳細モードのサブビュー（週間 or 毎時）
// ================================================================
enum class SubView {
  WEEKLY,
  HOURLY
};

// ================================================================
// 関数宣言
// ================================================================
void setupButtons();
void updateButtons();

// ================================================================
// 状態変数（button.cpp で実体定義）
// ================================================================
extern Screen      currentScreen;
extern DisplayMode currentMode;
extern SubView     currentSub;

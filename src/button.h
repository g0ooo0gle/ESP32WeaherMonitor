/**
 * button.h - 物理ボタン制御ヘッダ
 *
 * [ボタン構成] 3 ボタン・短押しのみ
 *
 *   ボタン名   GPIO  役割
 *   ──────────────────────────────────────────────────
 *   SCREEN      0    天気画面 ⇄ ニュース画面 切替
 *   MODE       35    地方巡回 ⇄ 自分の都市（天気画面のみ）
 *   NEXT       34    次へ（コンテキスト依存）
 *
 * [NEXT ボタンの動作]
 *   天気 / 地方巡回モード  → 次の都市（地方フィルタ順）
 *   天気 / 自分の都市モード → 週間予報 ⇄ 毎時予報 切替
 *   ニュース画面            → 次の見出しへ
 *
 * [GPIO 34・35 の注意]
 *   入力専用ピンのため内部プルアップが動作しません。
 *   10kΩ 抵抗で 3.3V に接続する外付けプルアップが必要です。
 */

#pragma once

#include "config.h"

// ================================================================
// ボタン GPIO ピン
// ================================================================
#define BTN_SCREEN_PIN   0
#define BTN_MODE_PIN    35
#define BTN_NEXT_PIN    34

#define BTN_DEBOUNCE_MS  50

// ================================================================
// 画面の種類
// ================================================================
enum class Screen {
  WEATHER,
  NEWS
};

// ================================================================
// WEATHER 画面内のモード
// ================================================================
enum class DisplayMode {
  ALL_CITIES,   // 地方巡回モード
  SINGLE        // 自分の都市モード
};

// ================================================================
// 自分の都市モードのサブビュー
// ================================================================
enum class SubView {
  WEEKLY,
  HOURLY
};

// ================================================================
// 関数宣言
// ================================================================
void setupButtons();
void updateButtons();

// ================================================================
// 状態変数（button.cpp で実体定義）
// ================================================================
extern Screen      currentScreen;
extern DisplayMode currentMode;
extern SubView     currentSub;

/**
 * ESP32 Weather Station - 物理ボタン制御 ヘッダ
 *
 * [ボタン構成] 3ボタン・短押しのみ・長押しなし
 *
 *   ボタン名   GPIO  役割
 *   ─────────────────────────────────────────────────────
 *   SCREEN     0     天気画面 ⇄ ニュース画面 切替
 *   MODE       35    地方巡回 ⇄ 自分の都市   切替（天気画面のみ）
 *   NEXT       34    次へ（コンテキスト依存）
 *
 * [NEXT ボタンの動作]
 *   天気 / 地方巡回モード  → 次の都市（地方フィルタに従う）
 *   天気 / 自分の都市モード → 週間予報 ⇄ 毎時予報 切替
 *   ニュース画面            → 次の見出しへ
 *
 * [GPIO 34 について]
 *   GPIO 34 は入力専用ピン（内部プルアップなし）。
 *   10kΩ の外付けプルアップ抵抗（→ 3.3V）が必要です。
 *   ※ ピンを変更する場合は BTN_NEXT_PIN の値を書き換えてください。
 */

#pragma once

#include "config.h"

// ================================================================
// ボタン GPIOピン
// ================================================================
#define BTN_SCREEN_PIN   0    // 天気 ⇄ ニュース（旧 MODE ボタン）
#define BTN_MODE_PIN    35    // 巡回 ⇄ 自分の都市（旧 CITY ボタン）
#define BTN_NEXT_PIN    34    // 次へ（新規ボタン、外付けプルアップ必要）

// チャタリング除去の待機時間
#define BTN_DEBOUNCE_MS  50

// ================================================================
// 画面の種類
// ================================================================
enum class Screen {
  WEATHER,   // 天気画面
  NEWS       // ニュース画面
};

// ================================================================
// WEATHER画面内のモード（全国 or 1都市）
// ================================================================
enum class DisplayMode {
  ALL_CITIES,   // 地方巡回モード
  SINGLE        // 自分の都市モード
};

// ================================================================
// 自分の都市モードのサブビュー（週間 or 毎時）
// ================================================================
enum class SubView {
  WEEKLY,
  HOURLY
};

// ================================================================
// 関数宣言
// ================================================================
void setupButtons();
void updateButtons();

// ================================================================
// 状態変数（button.cpp で実体定義）
// ================================================================
extern Screen      currentScreen;
extern DisplayMode currentMode;
extern SubView     currentSub;

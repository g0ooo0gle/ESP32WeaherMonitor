// ================================================================
// 天気コードから背景色を判定する関数
// 天気グループごとに背景色を返しています。
// ================================================================
uint16_t getBgColor(int code)
{
  if (code == 0)
    return COL_BG_CLEAR;    // 快晴
  if (code >= 1 && code <= 3)
    return COL_BG_CLOUDY;   // 晴れ・曇り
  if (code == 45 || code == 48)
    return COL_BG_FOG;      // 霧
  if ((code >= 51 && code <= 67) || (code >= 80 && code <= 82))
    return COL_BG_RAIN;     // 雨
  if ((code >= 71 && code <= 77) || (code >= 85 && code <= 86))
    return COL_BG_SNOW;     // 雪
  if (code >= 95)
    return COL_BG_THUNDER;  // 雷
  return ST77XX_BLACK;      // 不明なコードは黒で表示
}

// ================================================================
// [内部ヘルパー関数] 雲の形を描画する
//
// 複数の円を重ねて雲のシルエットを作ります。
// 引数 color でグレーの濃さを変えることで、晴れ曇・曇・暗い雲を使い分けます。
//
//   cx, cy : 雲の中心座標（雲全体の重心あたり）
//   color  : 雲の色（明るいグレー→暗いグレーなど）
// ================================================================
static void drawCloud(int cx, int cy, uint16_t color)
{
  // 雲は3つの円を横に並べて作ります。
  //   左の小さな円 → 中央の大きな円 → 右の小さな円
  // 中央の円だけ少し上にずらすことで、もこもこした雲らしい輪郭になります。
  tft.fillCircle(cx - 8, cy + 3,  6, color);   // 左の円（少し下）
  tft.fillCircle(cx,     cy - 1,  8, color);   // 中央の円（一番大きく、上にずらす）
  tft.fillCircle(cx + 8, cy + 3,  6, color);   // 右の円（左と対称）

  // 3つの円の下をつなぐ帯を描き、底辺を平らに見せる（雲の「腹」部分）
  // fillRect で各円の中央から下に向けて矩形を塗りつぶします。
  tft.fillRect(cx - 14, cy + 3, 28, 6, color);
}

// ================================================================
// [内部ヘルパー関数] 太陽の形を描画する
//
// 円（本体）＋8本の短い線（光線）で太陽を表現します。
//   cx, cy : 太陽の中心座標
// ================================================================
static void drawSun(int cx, int cy)
{
  // 太陽本体（オレンジ色の円）
  tft.fillCircle(cx, cy, 8, ST77XX_ORANGE);

  // 放射状の光線を8方向（0°〜315°、45°刻み）に描画します。
  // DEG_TO_RAD は Adafruit_GFX に付属の定数（度→ラジアン変換用）
  for (int deg = 0; deg < 360; deg += 45)
  {
    float rad = deg * DEG_TO_RAD;
    // cos/sin で角度から方向ベクトルを計算。
    // 内側（半径11）から外側（半径15）まで線を引く。
    tft.drawLine(
      cx + (int)(cos(rad) * 11),   // 線の始点 X
      cy + (int)(sin(rad) * 11),   // 線の始点 Y
      cx + (int)(cos(rad) * 15),   // 線の終点 X
      cy + (int)(sin(rad) * 15),   // 線の終点 Y
      ST77XX_YELLOW
    );
  }
}

// ================================================================
// 天気アイコン描画関数（図形プリミティブによる描画）
//
// 描画領域: (x, y) を左上とした 36×36 px の範囲内に収めています。
// Adafruit_GFX の描画関数（円・線・三角・矩形）だけで完結させており、
// 外部の画像ファイルやビットマップに依存しません（組み込み向けの方針）。
//
// [対応グループ一覧]
//   code 0        : 快晴（太陽）
//   code 1        : 概ね晴れ（太陽＋薄い雲）
//   code 2〜3     : 曇り（明るい雲・暗い雲）
//   code 45〜48   : 霧（横線3本）  ← Phase2で専用アイコンに変更
//   code 51〜67   : 雨（雲＋雨粒）
//   code 71〜77   : 雪（雲＋雪の結晶風の点）  ← Phase2で専用アイコンに変更
//   code 80〜82   : にわか雨（雨と同じアイコン）
//   code 85〜86   : 雪のシャワー（雪と同じアイコン）
//   code 95〜99   : 雷雨（黒雲＋稲妻）
// ================================================================
void drawWeatherIcon(int x, int y, int code)
{
  // -----------------------------------------
  // code 0: 快晴 → 太陽（光線あり）
  // -----------------------------------------
  if (code == 0)
  {
    // 中央に太陽を描画（座標はアイコン領域の中央: x+18, y+18）
    drawSun(x + 18, y + 18);
  }

  // -----------------------------------------
  // code 1: 概ね晴れ → 太陽＋薄い雲
  //   太陽を左上寄りに描き、右下に明るい雲を重ねます。
  //   「晴れ」と「曇り」の中間表現です。
  // -----------------------------------------
  else if (code == 1)
  {
    // 太陽は左上寄りに（雲と重なるように少しずらす）
    drawSun(x + 12, y + 13);

    // 太陽の手前（右下）に明るいグレーの雲を描く。
    // 太陽の一部が雲に隠れるように、雲をやや前面（後から描画）に配置します。
    drawCloud(x + 20, y + 24, 0xC618);   // 0xC618: 明るいグレー（RGB565形式）
  }

  // -----------------------------------------
  // code 2: 時々曇り → 明るい雲
  // code 3: くもり   → 暗い雲
  //   code が大きいほど暗い色を使い、曇り度を表現します。
  // -----------------------------------------
  else if (code == 2)
  {
    // 明るいグレーの雲（0xC618）で「薄曇り」を表現
    drawCloud(x + 18, y + 20, 0xC618);
  }
  else if (code == 3)
  {
    // 暗いグレーの雲（0x8410）で「どんより曇り」を表現
    drawCloud(x + 18, y + 20, 0x8410);
  }

  // -----------------------------------------
  // code 45, 48: 霧 → 横線3本
  //   霧は視界が遮られるイメージを、均等に並んだ横線で表現します。
  //   旧実装では雪と霧を同じアイコンで描いていましたが、分離しました。
  // -----------------------------------------
  else if (code == 45 || code == 48)
  {
    // 薄いグレーの横線を3本、縦に均等間隔（8px）で並べます。
    // drawRoundRect で端を丸くして、霧のぼんやりした質感を出します。
    //   引数: (x, y, 幅, 高さ, 角の丸み半径, 色)
    for (int i = 0; i < 3; i++)
    {
      // i=0: y+12、i=1: y+20、i=2: y+28 の位置に描画
      tft.drawRoundRect(x + 4, y + 12 + i * 8, 28, 3, 1, 0x8410);   // 暗いグレー
    }
  }

  // -----------------------------------------
  // code 51〜67: 霧雨・雨（弱〜強）
  // code 80〜82: にわか雨（弱〜激しい）
  //   雲＋斜め雨粒で表現。code が大きいほど雨粒の数を増やします。
  // -----------------------------------------
  else if ((code >= 51 && code <= 67) || (code >= 80 && code <= 82))
  {
    // 雲を上側に描画（暗いグレー）
    drawCloud(x + 18, y + 14, 0x8410);

    // 雨粒の本数を天気の強さで変える
    //   弱い雨（51〜53, 61, 80）: 2本
    //   普通の雨（54〜55, 63, 81）: 3本
    //   強い雨（56〜67, 82）: 4本
    int drops;
    if (code == 51 || code == 61 || code == 80)
      drops = 2;
    else if (code == 53 || code == 63 || code == 81)
      drops = 3;
    else
      drops = 4;

    // 雨粒を左から均等に並べて描画。斜め（右下方向）に引いて動きを表現します。
    for (int i = 0; i < drops; i++)
    {
      int rx = x + 8 + i * 7;   // 各雨粒のX開始位置（7px間隔）
      // drawLine(始点X, 始点Y, 終点X, 終点Y, 色)
      // 始点より終点を少し右にずらすと「斜め雨」に見えます。
      tft.drawLine(rx, y + 24, rx - 2, y + 31, ST77XX_CYAN);
    }
  }

  // -----------------------------------------
  // code 71〜77: 雪（弱〜強）
  // code 85〜86: 雪のシャワー
  //   雲＋雪の結晶を模した点（＊マーク風）で表現します。
  //   旧実装では霧と同じアイコンでしたが、専用アイコンに分離しました。
  // -----------------------------------------
  else if ((code >= 71 && code <= 77) || (code >= 85 && code <= 86))
  {
    // 雲を上側に描画（やや青みがかった薄いグレー）
    drawCloud(x + 18, y + 14, 0x9CF3);   // 0x9CF3: 青みがかったグレー（雪雲っぽい色）

    // 雪の結晶を小さな＋マーク（十字）と斜め線で描きます。
    // 3個の結晶を横に並べます。
    int snowX[] = {x + 9, x + 18, x + 27};   // 結晶3個のX中心座標
    for (int i = 0; i < 3; i++)
    {
      int sx = snowX[i];
      int sy = y + 29;   // すべて同じY座標（雲の下）

      // 縦線（↕）
      tft.drawLine(sx, sy - 3, sx, sy + 3, ST77XX_WHITE);
      // 横線（↔）
      tft.drawLine(sx - 3, sy, sx + 3, sy, ST77XX_WHITE);
      // 斜め線（＼方向）
      tft.drawLine(sx - 2, sy - 2, sx + 2, sy + 2, 0xB5B6);   // 薄いグレー（控えめに）
      // 斜め線（／方向）
      tft.drawLine(sx + 2, sy - 2, sx - 2, sy + 2, 0xB5B6);
    }
  }

  // -----------------------------------------
  // code 95〜99: 雷雨（弱〜強）
  //   黒雲＋稲妻（ジグザグの三角形2つ）で表現します。
  // -----------------------------------------
  else if (code >= 95)
  {
    // 黒雲（暗い灰色）を上側に描画
    drawCloud(x + 18, y + 14, 0x4208);   // 0x4208: 非常に暗いグレー（ほぼ黒）

    // 稲妻を2つの三角形のジグザグで描きます。
    //   上の三角形: 雲の下から斜め左下へ
    //   下の三角形: 上の先端から続いて更に左下へ
    // これを重ねることでZ字型の稲妻になります。
    tft.fillTriangle(
      x + 21, y + 20,   // 頂点（雲のすぐ下・右寄り）
      x + 14, y + 29,   // 左下
      x + 20, y + 29,   // 右下（三角形の底辺）
      ST77XX_YELLOW
    );
    tft.fillTriangle(
      x + 19, y + 27,   // 上の三角形の底辺と接続する点（左）
      x + 24, y + 27,   // 上の三角形の底辺と接続する点（右）
      x + 15, y + 36,   // 稲妻の先端（一番下）
      ST77XX_YELLOW
    );
  }

  // -----------------------------------------
  // その他（未定義コード）→ 疑問符「?」を表示
  //   将来WMOが新しいコードを追加した場合や、APIのバグへのフォールバック。
  // -----------------------------------------
  else
  {
    // 白い「?」を中央に描画（テキストサイズ2 = 12×16px相当）
    tft.setTextColor(ST77XX_WHITE);
    tft.setTextSize(2);
    tft.setCursor(x + 13, y + 12);
    tft.print("?");
  }
}

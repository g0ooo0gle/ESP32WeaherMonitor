/**
 * ESP32 Weather Station - 都市データ
 *
 * Open-Meteo API から天気を取得する対象都市のリストです。
 * この配列を追加・削除することで、表示対象都市を簡単に変更できます。
 * 緯度・経度はOpen-Meteo APIに準拠した値を使用しています。
 */

#ifndef CITIES_H
#define CITIES_H

// ------------------------------------------------------------------
// 都市データ構造体
// 1都市ごとに「名前」「緯度」「経度」の情報を保持します。
// ------------------------------------------------------------------
struct CityData
{
  const char *name;  // 都市の名前（日本語）
  float lat;         // 緯度（Open-Meteo API用）
  float lon;         // 経度（Open-Meteo API用）
};

// ------------------------------------------------------------------
// 都市リスト
// 北海道から沖縄まで、日本の主要10都市を地方別に登録しています。
// 配列の要素数は cityCount 変数で自動計算されます。
// ------------------------------------------------------------------
extern const CityData cities[];
extern const int cityCount;

#endif // CITIES_H

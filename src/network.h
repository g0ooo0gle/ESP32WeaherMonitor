/**
 * ESP32 Weather Station - ネットワーク・通信機能
 *
 * このファイルでは以下の3つのネットワーク関連機能を実装しています:
 *
 *   1. WiFiManager によるWi-Fi接続（自動接続 or APモード）
 *   2. NTP サーバーによる時刻同期
 *   3. Open-Meteo API による天気データ取得
 *
 * WiFi接続や通信が不安定な場合でも、できるだけ安全に動作するよう
 // タイムアウト設定やエラーハンドリングを施しています。
 */

#ifndef NETWORK_H
#define NETWORK_H

#include "config.h"

// ------------------------------------------------------------------
// Open-Meteo API から天気データを取得
//
// Wi-Fi に接続されていることを前提としています。未接続の場合は
 // 何もせず false を返します。
//
// 戻り値: データが実際に変化した場合 true、それ以外（未接続・エラー・未変化）は false
//         loop() 側ではこの戻り値を見て描画の有無を判定しています。
// ------------------------------------------------------------------
bool updateWeather();

// ------------------------------------------------------------------
// WiFiManager による自動接続
//
// 事前に設定済みのSSID/パスワードがあれば自動接続します。
// 未設定の場合は "ESP32_Weather_Config" というアクセスポイント（AP）
// を作成し、スマホのブラウザから設定できるようになります。
// （接続失敗または AP設定後 disconnect で false を返します）
// ------------------------------------------------------------------
bool setupWiFi();

// ------------------------------------------------------------------
// NTP による時刻同期設定
//
// ESP32 の内部クロックを NTP サーバーと同期して、正確な時刻を
// 得られるように設定します。
// ------------------------------------------------------------------
void setupNTP();

#endif // NETWORK_H

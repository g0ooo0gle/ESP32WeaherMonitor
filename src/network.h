/**
 * ESP32 Weather Station - ネットワーク・通信機能
 *
 * [変更点]
 *   - updateWeeklyForecast() を新設（Open-Meteo daily API で週間予報を取得）
 */

#ifndef NETWORK_H
#define NETWORK_H

#include "config.h"

// ------------------------------------------------------------------
// WiFiManager による自動接続
// 保存済みのSSID/パスワードがあれば自動接続。
// 未設定の場合は "ESP32_Weather_Config" という AP を起動し、
// スマホのブラウザ（192.168.4.1）から設定できるようになります。
// 戻り値: 接続成功 true / 失敗 false
// ------------------------------------------------------------------
bool setupWiFi();

// ------------------------------------------------------------------
// NTP による時刻同期設定
// NICT / Google の NTP サーバーに接続し、JST（UTC+9）を取得します。
// ------------------------------------------------------------------
void setupNTP();

// ------------------------------------------------------------------
// Open-Meteo API から現在天気データを取得
// 戻り値: データが実際に変化した場合 true（loop()での再描画判定に使用）
// ------------------------------------------------------------------
bool updateWeather();

// ------------------------------------------------------------------
// Open-Meteo API から週間天気予報データを取得（新規追加）
//
// daily パラメータで最大7日分の最高・最低気温と天気コードを取得します。
// 取得結果は weeklyForecast[] に格納されます。
// 戻り値: 取得成功 true / 失敗 false
// ------------------------------------------------------------------
bool updateWeeklyForecast();

#endif // NETWORK_H
